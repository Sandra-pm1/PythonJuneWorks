#Relational operators(for comparison)

# <   less than
# <=  less than or equal
# >   greater than
# >=  greater than or equal
# ==  equal
# !=  not equal 



num1=100
num2=200

print(num1<num2)
print(num1<=num2)
print(num1>num2)
print(num1>=num2)
print(num1==num2)
print(num1!=num2)
print(num1%100==0)  #to find num1 ends with 00(century year)
print(num2%2==0)
print(num1%2!=0)